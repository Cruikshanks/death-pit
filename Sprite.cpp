//////////////////////////////////////////////////////
// CSprite.cpp
//////////////////////////////////////////////////////

#include "sprite.h"

//////////////////////////////////////////////////////
// CSprite()
//////////////////////////////////////////////////////
CSprite::CSprite(int iWidth, int iHeight, CSprite::SPRITE_TYPE SpriteType)
{
	m_iWidth = iWidth;
	m_iHeight = iHeight;
	m_fXPos = 0;
	m_fYPos = 0;
	m_fSpeed = 0;
	m_fHit = FALSE;
	m_Rect.left = 0;
	m_Rect.right = m_iWidth;
	m_Rect.top = 0;
	m_Rect.bottom = m_iHeight;
	m_SpriteType = SpriteType;
}

//////////////////////////////////////////////////////
// ~CSprite()
//////////////////////////////////////////////////////
CSprite::~CSprite(void)
{
}

//////////////////////////////////////////////////////
// SetPosition()
//////////////////////////////////////////////////////
void CSprite::SetPosition(float fXPos, float fYPos)
{
	SetXPosition(fXPos);
	SetYPosition(fYPos);
}

//////////////////////////////////////////////////////
// SetXPosition()
//////////////////////////////////////////////////////
void CSprite::SetXPosition(float fXPos)
{
	m_fXPos = fXPos;
}

//////////////////////////////////////////////////////
// GetXPosition()
//////////////////////////////////////////////////////
float CSprite::GetXPosition(void)
{
	return m_fXPos;
}

//////////////////////////////////////////////////////
// SetYPosition()
//////////////////////////////////////////////////////
void CSprite::SetYPosition(float fYPos)
{
	m_fYPos = fYPos;
}

//////////////////////////////////////////////////////
// GetYPosition()
//////////////////////////////////////////////////////
float CSprite::GetYPosition(void)
{
	return m_fYPos;
}

//////////////////////////////////////////////////////
// SetSpeed()
//////////////////////////////////////////////////////
void CSprite::SetSpeed(float fSpeed)
{
	m_fSpeed = fSpeed;
}

//////////////////////////////////////////////////////
// GetSpeed()
//////////////////////////////////////////////////////
float CSprite::GetSpeed(void)
{
	return m_fSpeed;
}

//////////////////////////////////////////////////////
// SetHit()
//////////////////////////////////////////////////////
void CSprite::SetHit(BOOL fHit)
{
	m_fHit = fHit;
}

//////////////////////////////////////////////////////
// GetHit()
//////////////////////////////////////////////////////
BOOL CSprite::GetHit(void)
{
	return m_fHit;
}

//////////////////////////////////////////////////////
// SetRECT()
//////////////////////////////////////////////////////
void CSprite::SetRECT(RECT rect)
{
	m_Rect = rect;
}

//////////////////////////////////////////////////////
// GetRECT()
//////////////////////////////////////////////////////
RECT CSprite::GetRECT(void)
{
	return m_Rect;
}

//////////////////////////////////////////////////////
// SetRECTValues()
//////////////////////////////////////////////////////
void CSprite::SetRECTValues(LONG lLeft, LONG lRight, LONG lTop, LONG lBottom)
{
	m_Rect.left = lLeft;
	m_Rect.right = lRight;
	m_Rect.top = lTop;
	m_Rect.bottom = lBottom;
}

//////////////////////////////////////////////////////
// GetWidth()
//////////////////////////////////////////////////////
int CSprite::GetWidth(void)
{
	return m_iWidth;
}

//////////////////////////////////////////////////////
// GetHeight()
//////////////////////////////////////////////////////
int CSprite::GetHeight(void)
{
	return m_iHeight;
}

//////////////////////////////////////////////////////
// GetSpriteType()
//////////////////////////////////////////////////////
CSprite::SPRITE_TYPE CSprite::GetSpriteType(void)
{
	return m_SpriteType;
}